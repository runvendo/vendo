describe("parseDemoDeployArgs", () => {
  it("parses a fully specified deploy", () => {
    expect(parseDemoDeployArgs([
      "--app", "apps/demo-linear",
      "--project", "other-project",
      "--router-url", "https://router.example",
      "--skip-registry",
      "--dry-run",
    ])).toEqual({
      app: "apps/demo-linear",
      project: "other-project",
      routerUrl: "https://router.example",
      skipRegistry: true,
      dryRun: true,
    });
  });

  it("defaults project, router url, and both flags", () => {
    expect(parseDemoDeployArgs(["--app", "apps/demo-linear"])).toEqual({
      app: "apps/demo-linear",
      project: "vendo-demos",
      routerUrl: "https://demos.vendo.run",
      skipRegistry: false,
      dryRun: false,
    });
  });

  it("accepts the literal separator forwarded by pnpm scripts", () => {
    expect(parseDemoDeployArgs(["--", "--app", "apps/demo-linear"])).toMatchObject({ app: "apps/demo-linear" });
  });

  it("requires --app and rejects unknown options and missing values", () => {
    expect(() => parseDemoDeployArgs([])).toThrow("--app is required");
    expect(() => parseDemoDeployArgs(["--app", "apps/x", "--nope"])).toThrow("Unknown option: --nope");
    expect(() => parseDemoDeployArgs(["--app"])).toThrow("--app requires a value");
    expect(() => parseDemoDeployArgs(["--app", "apps/x", "--router-url", "not a url"]))
      .toThrow("--router-url");
  });

  it("requires an https router URL, except for localhost development", () => {
    expect(() => parseDemoDeployArgs(["--app", "apps/x", "--router-url", "http://router.example"]))
      .toThrow(/--router-url must be https/);
    expect(parseDemoDeployArgs(["--app", "apps/x", "--router-url", "http://localhost:8080"]).routerUrl)
      .toBe("http://localhost:8080");
    expect(parseDemoDeployArgs(["--app", "apps/x", "--router-url", "http://127.0.0.1:8080"]).routerUrl)
      .toBe("http://127.0.0.1:8080");
  });

  it("normalizes a trailing slash off the router URL (admin paths append cleanly)", () => {
    expect(parseDemoDeployArgs(["--app", "apps/x", "--router-url", "https://router.example/"]).routerUrl)
      .toBe("https://router.example");
  });
});

describe("Dockerfile rendering", () => {
  it("parametrizes demo-bank's pattern: turbo filter, app WORKDIR, port 3000, marker", () => {
    const dockerfile = renderDockerfile({ packageName: "demo-linear", appPath: "apps/demo-linear" });
    expect(dockerfile).toContain(GENERATED_FILE_MARKER);
    expect(dockerfile).toContain("FROM node:22-bookworm-slim");
    expect(dockerfile).toContain("pnpm exec turbo run build --filter=demo-linear");
    expect(dockerfile).toContain("WORKDIR /app/apps/demo-linear");
    expect(dockerfile).toContain("EXPOSE 3000");
    expect(dockerfile).toContain('CMD ["pnpm", "start"]');
  });

  it("shouldWriteGeneratedFile: absent or previously generated by us, never a hand-edited file", () => {
    expect(shouldWriteGeneratedFile(undefined)).toBe(true);
    expect(shouldWriteGeneratedFile(`# hello\n${GENERATED_FILE_MARKER}\nFROM x\n`)).toBe(true);
    expect(shouldWriteGeneratedFile("FROM node:22\n# hand-written\n")).toBe(false);
  });
});

describe("parseRailwayDomain", () => {
  // Samples are synthetic (probing `railway domain` live creates domains);
  // the parser is deliberately liberal: JSON first, then a domain regex.
  it("parses a --json object payload", () => {
    expect(parseRailwayDomain('{"domain":"demo-linear-production.up.railway.app"}'))
      .toBe("demo-linear-production.up.railway.app");
  });

  it("parses JSON embedded among log lines", () => {
    const output = 'Creating domain...\n{"domain":"https://demo-x.up.railway.app/"}\nDone\n';
    expect(parseRailwayDomain(output)).toBe("demo-x.up.railway.app");
  });

  it("falls back to finding a railway domain in plain text", () => {
    expect(parseRailwayDomain("Service Domain created:\n🚀 https://demo-linear-production.up.railway.app\n"))
      .toBe("demo-linear-production.up.railway.app");
  });

  it("returns undefined when no domain is present", () => {
    expect(parseRailwayDomain("no domains here")).toBeUndefined();
    expect(parseRailwayDomain("")).toBeUndefined();
  });
});

