describe("buildDeployPlan", () => {
  const plan = buildDeployPlan({
    serviceName: "demo-linear",
    project: "vendo-demos",
    dockerfilePath: "apps/demo-linear/Dockerfile",
    anthropicApiKey: "sk-ant-SECRET",
  });

  it("drives the verified railway CLI syntax in order: link, add, variables, up, domain", () => {
    expect(plan.map((step) => step.command.slice(0, 2).join(" "))).toEqual([
      "railway link",
      "railway add",
      "railway variables",
      "railway up",
      "railway domain",
    ]);
    const [link, add, variables, up, domain] = plan;
    expect(link?.command).toEqual(["railway", "link", "--project", "vendo-demos"]);
    expect(add?.command).toEqual(["railway", "add", "--service", "demo-linear", "--json"]);
    expect(variables?.command).toEqual([
      "railway", "variables",
      "--set", "ANTHROPIC_API_KEY=sk-ant-SECRET",
      "--set", "NODE_ENV=production",
      "--set", "RAILWAY_DOCKERFILE_PATH=apps/demo-linear/Dockerfile",
      "--service", "demo-linear",
      "--skip-deploys",
    ]);
    expect(up?.command).toEqual(["railway", "up", "--service", "demo-linear", "--detach"]);
    expect(domain?.command).toEqual(["railway", "domain", "--service", "demo-linear", "--json"]);
  });

  it("NEVER exposes the anthropic key in the display rendering", () => {
    for (const step of plan) {
      expect(step.display).not.toContain("sk-ant-SECRET");
    }
    expect(plan[2]?.display).toContain("ANTHROPIC_API_KEY=<redacted>");
  });

  it("tolerates `railway add` failing when the service already exists", () => {
    expect(plan[1]?.allowFailure).toBe(true);
    expect(plan.filter((step) => step.allowFailure !== true)).toHaveLength(4);
  });
});

describe("runDemoDeploy (dry run against a fixture app)", () => {
  const demoConfig = {
    id: "linear",
    prospect: "Linear",
    ctaUrl: "https://cal.com/yousefhelal",
    beats: [{ key: "generate-ui", prompt: "Show me", chip: "Show me" }],
    caps: { maxTurns: 20, maxSpendUsd: 5 },
    expiresAt: "2026-08-01T00:00:00Z",
  };

  async function writeFixture(): Promise<{ repoRoot: string; appDir: string }> {
    const repoRoot = await mkdtemp(path.join(tmpdir(), "vendo-demo-deploy-"));
    const appDir = path.join(repoRoot, "apps", "demo-linear");
    await mkdir(appDir, { recursive: true });
    await writeFile(path.join(appDir, "package.json"), `${JSON.stringify({ name: "demo-linear", private: true }, null, 2)}\n`);
    await writeFile(path.join(appDir, "demo.config.json"), `${JSON.stringify(demoConfig, null, 2)}\n`);
    return { repoRoot, appDir };
  }

  const args = {
    app: "apps/demo-linear",
    project: "vendo-demos",
    routerUrl: "https://demos.vendo.run",
    skipRegistry: false,
    dryRun: true,
  };

  it("validates the config, renders the Dockerfile, and prints the plan without executing", async () => {
    const { repoRoot, appDir } = await writeFixture();
    const exec = vi.fn();
    const fetchImpl = vi.fn();
    const lines: string[] = [];
    const result = await runDemoDeploy(args, {
      repoRoot,
      exec,
      fetchImpl: fetchImpl as unknown as typeof fetch,
      env: {},
      write: (line) => lines.push(line),
    });
    expect(result.dryRun).toBe(true);
    expect(result.serviceName).toBe("demo-linear");
    expect(exec).not.toHaveBeenCalled();
    expect(fetchImpl).not.toHaveBeenCalled();
    const dockerfile = await readFile(path.join(appDir, "Dockerfile"), "utf8");
    expect(dockerfile).toContain(GENERATED_FILE_MARKER);
    expect(dockerfile).toContain("--filter=demo-linear");
    // The upload-exclusion mechanism is the repo-root .dockerignore; a
    // per-app one would be inert (Docker only honors the context root's).
    expect(existsSync(path.join(appDir, ".dockerignore"))).toBe(false);
    const output = lines.join("\n");
    expect(output).toContain("railway up --service demo-linear --detach");
    expect(output).toContain("POST https://demos.vendo.run/admin/demos");
    expect(output).not.toContain("sk-ant");
  });

  it("keeps a hand-edited Dockerfile instead of clobbering it", async () => {
    const { repoRoot, appDir } = await writeFixture();
    await writeFile(path.join(appDir, "Dockerfile"), "FROM scratch\n# hand-tuned\n");
    await runDemoDeploy(args, { repoRoot, env: {}, write: () => {} });
    expect(await readFile(path.join(appDir, "Dockerfile"), "utf8")).toBe("FROM scratch\n# hand-tuned\n");
  });

  it("refuses an app directory outside the repo root (the build context)", async () => {
    const { repoRoot } = await writeFixture();
    await expect(runDemoDeploy({ ...args, app: "../elsewhere" }, { repoRoot, env: {}, write: () => {} }))
      .rejects.toThrow(/inside the repo/);
  });

  it("refuses to run live without ANTHROPIC_API_KEY", async () => {
    const { repoRoot } = await writeFixture();
    await expect(runDemoDeploy({ ...args, dryRun: false }, { repoRoot, env: {}, write: () => {} }))
      .rejects.toThrow(/ANTHROPIC_API_KEY/);
  });

  it("surfaces a broken demo.config.json loudly", async () => {
    const { repoRoot, appDir } = await writeFixture();
    await writeFile(path.join(appDir, "demo.config.json"), JSON.stringify({ ...demoConfig, id: "Not A Slug" }));
    await expect(runDemoDeploy(args, { repoRoot, env: {}, write: () => {} }))
      .rejects.toThrow(/id: must be lowercase alphanumeric with hyphens/);
  });

  describe("live path (mock exec)", () => {
    const env = { ANTHROPIC_API_KEY: "sk-ant-SECRET", ROUTER_ADMIN_TOKEN: "token" };
    const liveArgs = { ...args, dryRun: false };

    function happyExec() {
      return vi.fn().mockImplementation(async (command: string[]) => ({
        code: 0,
        stdout: command[1] === "domain" ? '{"domain":"demo-linear-production.up.railway.app"}' : "",
        stderr: "",
      }));
    }

    it("runs the lockfile preflight, then the railway plan, then registers with the router", async () => {
      const { repoRoot } = await writeFixture();
      const exec = happyExec();
      const fetchImpl = vi.fn().mockResolvedValue(new Response("{}", { status: 200 }));
      const result = await runDemoDeploy(liveArgs, {
        repoRoot,
        exec,
        fetchImpl: fetchImpl as unknown as typeof fetch,
        env,
        write: () => {},
      });
      const commands = exec.mock.calls.map(([command]) => (command as string[]).join(" "));
      // `railway up` archives the working tree, so the lockfile must be
      // synced BEFORE any railway step (untracked scratch apps otherwise
      // break --frozen-lockfile inside the image build).
      expect(commands[0]).toBe("pnpm install --lockfile-only");
      expect(commands.slice(1)).toEqual([
        "railway link --project vendo-demos",
        "railway add --service demo-linear --json",
        "railway variables --set ANTHROPIC_API_KEY=sk-ant-SECRET --set NODE_ENV=production --set RAILWAY_DOCKERFILE_PATH=apps/demo-linear/Dockerfile --service demo-linear --skip-deploys",
        "railway up --service demo-linear --detach",
        "railway domain --service demo-linear --json",
      ]);
      expect(result.domain).toBe("demo-linear-production.up.railway.app");
      expect(fetchImpl).toHaveBeenCalledWith("https://demos.vendo.run/admin/demos", expect.objectContaining({ method: "POST" }));
      expect(result.registryPayload).toEqual({
        id: "linear",
        url: "https://demo-linear-production.up.railway.app",
        prospect: "Linear",
        expiresAt: "2026-08-01T00:00:00Z",
      });
    });

    it("scrubs the anthropic key out of echoed child output on failure", async () => {
      const { repoRoot } = await writeFixture();
      const exec = vi.fn().mockImplementation(async (command: string[]) =>
        command[1] === "up"
          ? { code: 1, stdout: "", stderr: "boom: variables were ANTHROPIC_API_KEY=sk-ant-SECRET etc" }
          : { code: 0, stdout: "", stderr: "" });
      const lines: string[] = [];
      const error = await runDemoDeploy(liveArgs, {
        repoRoot,
        exec,
        env,
        write: (line) => lines.push(line),
      }).catch((caught: unknown) => caught as Error);
      expect(error).toBeInstanceOf(Error);
      expect((error as Error).message).toContain("<redacted>");
      expect((error as Error).message).not.toContain("sk-ant-SECRET");
      expect(lines.join("\n")).not.toContain("sk-ant-SECRET");
    });

    it("scrubs tolerated-failure output too (railway add when the service exists)", async () => {
      const { repoRoot } = await writeFixture();
      const exec = vi.fn().mockImplementation(async (command: string[]) => {
        if (command[1] === "add") return { code: 1, stdout: "", stderr: "exists; env has ANTHROPIC_API_KEY=sk-ant-SECRET" };
        return {
          code: 0,
          stdout: command[1] === "domain" ? '{"domain":"demo-linear.up.railway.app"}' : "",
          stderr: "",
        };
      });
      const lines: string[] = [];
      await runDemoDeploy({ ...liveArgs, skipRegistry: true }, { repoRoot, exec, env, write: (line) => lines.push(line) });
      const output = lines.join("\n");
      expect(output).toContain("<redacted>");
      expect(output).not.toContain("sk-ant-SECRET");
    });
  });
});
