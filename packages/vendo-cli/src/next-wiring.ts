/**
 * Merge the scheduler-boot `register()` into an existing `instrumentation.ts`
 * non-destructively. Returns the merged source, the input unchanged when
 * already wired (idempotent), or `null` when the file already declares its
 * own `register()` — Next.js recognizes only one, so injecting into someone
 * else's function body would require guessing at its shape; the caller falls
 * back to `instrumentation.vendo-example.ts` + a manual step instead.
 */
export function mergeInstrumentation(source: string): string | null {
  const masked = maskLiterals(source);
  if (masked.includes("startVendoScheduler")) return source; // already wired
  if (/\bfunction\s+register\s*\(|\bconst\s+register\s*=/.test(masked)) return null;
  return source.replace(/\n?$/, "\n\n") + INSTRUMENTATION_SOURCE;
}

/**
 * Wrap the single `{children}` expression of the root layout with the
 * generated provider. Returns the edited source, or null when the file cannot
 * be edited with certainty (caller falls back to manual instructions).
 *
 * Targets the standard layout shape (one JSX `{children}` in child position).
 * Known limitation: a `{children}` nested inside a JSX ATTRIBUTE expression
 * (e.g. `<Chrome slot={<b>{children}</b>}>...`) is indistinguishable from a
 * real child by this heuristic and would be wrapped in the wrong place — such
 * exotic layouts should be wired by hand (the whole edit is one reviewable
 * diff). create-next-app and typical provider layouts are handled correctly.
 */
/**
 * Return a copy of `source` with the CONTENTS of string/template literals and
 * comments blanked to spaces (quotes/delimiters kept, lengths and offsets
 * preserved). We locate `{children}` and detect idempotence against THIS
 * masked view so a `{children}` sitting inside a string (`"<b>{children}</b>"`)
 * or a comment (`// rename AppVendoRoot`) can never be matched or mistaken
 * for existing wiring — then splice edits back into the ORIGINAL by offset.
 */
export function maskLiterals(source: string): string {
  const out = source.split("");
  let i = 0;
  const n = source.length;
  const blank = (from: number, to: number) => {
    for (let k = from; k < to && k < n; k++) if (source[k] !== "\n") out[k] = " ";
  };
  while (i < n) {
    const c = source[i];
    const next = source[i + 1];
    if (c === "/" && next === "/") {
      let j = i + 2;
      while (j < n && source[j] !== "\n") j++;
      blank(i, j);
      i = j;
    } else if (c === "/" && next === "*") {
      let j = i + 2;
      while (j < n && !(source[j] === "*" && source[j + 1] === "/")) j++;
      blank(i, Math.min(j + 2, n));
      i = j + 2;
    } else if (c === '"' || c === "'" || c === "`") {
      const quote = c;
      let j = i + 1;
      while (j < n) {
        if (source[j] === "\\") { j += 2; continue; }
        if (source[j] === quote) break;
        j++;
      }
      blank(i + 1, j); // keep the delimiters, blank the interior
      i = j + 1;
    } else {
      i++;
    }
  }
  return out.join("");
}

/**
 * Insert `importStmt` at an ALWAYS-VALID position: immediately after a leading
 * "use client"/"use server" directive if present, else at the very top.
 * (Anchoring after "the last import" is unsafe — a Prettier-wrapped multi-line
 *  import would land the new line INSIDE the brace block.)
 *
 * A directive must remain the FIRST statement, but Next allows comments above
 * it — so skip leading comments/whitespace (found on the masked copy, which has
 * comment CONTENT blanked but delimiters/offsets intact) before checking, then
 * detect the directive on the ORIGINAL at that offset (the mask also blanks the
 * "use client" string interior).
 */
export function insertImportAfterDirectives(source: string, importStmt: string): string {
  const headerLen = maskLiterals(source).match(/^\s*/)?.[0].length ?? 0;
  const directive = source.slice(headerLen).match(/^(['"]use [a-z ]+['"];?[^\n]*\n)/);
  if (directive) {
    const end = headerLen + directive[0].length;
    return source.slice(0, end) + importStmt + source.slice(end);
  }
  return importStmt + source;
}

export function wrapLayoutChildren(source: string, componentName = "AppVendoRoot"): string | null {
  const masked = maskLiterals(source);
  // Idempotence: already wired (by us or by hand). Checked against the masked
  // view so an incidental mention in a comment/string doesn't false-positive.
  if (masked.includes('from "./vendo-root"') || masked.includes(`<${componentName}>`)) {
    return source;
  }

  // Candidate `{children}` expressions in real code (not strings/comments),
  // filtered to JSX child position: the preceding non-whitespace char must be
  // `>` (end of an opening tag). This excludes destructured parameters
  // `({ children })` and type annotations.
  const matches = [...masked.matchAll(/\{\s*children\s*\}/g)].filter((m) => {
    const before = masked.slice(0, m.index).trimEnd();
    return before.endsWith(">");
  });
  if (matches.length !== 1) return null; // zero or ambiguous — do not guess

  const match = matches[0]!;
  const start = match.index;
  const original = source.slice(start, start + match[0].length); // exact orig text
  const wrapped =
    source.slice(0, start) +
    `<${componentName}>` +
    original +
    `</${componentName}>` +
    source.slice(start + match[0].length);

  const importStmt = `import { ${componentName} } from "./vendo-root";\n`;
  return insertImportAfterDirectives(wrapped, importStmt);
}

/** Merge `vendoai` into package.json dependencies (format-preserving-ish:
 *  2-space JSON, trailing newline — the npm convention). */
export function addDependency(pkgJson: string, name: string, version: string): string | null {
  let pkg: Record<string, unknown>;
  try {
    pkg = JSON.parse(pkgJson) as Record<string, unknown>;
  } catch {
    return null;
  }
  const deps = (pkg["dependencies"] ?? {}) as Record<string, string>;
  if (deps[name]) return pkgJson; // already present — leave the file alone
  pkg["dependencies"] = Object.fromEntries(
    Object.entries({ ...deps, [name]: version }).sort(([a], [b]) => a.localeCompare(b)),
  );
  return JSON.stringify(pkg, null, 2) + "\n";
}

/**
 * AI SDK v6 imports both `zod/v3` and `zod/v4`. If a host app already depends
 * on older Zod 3, Next resolves those imports against the host copy and the
 * server build fails. Keep host apps on Zod 3, but require the release line
 * that exposes those subpaths.
 */
export function ensureAiSdkZodDependency(pkgJson: string): string | null {
  let pkg: Record<string, unknown>;
  try {
    pkg = JSON.parse(pkgJson) as Record<string, unknown>;
  } catch {
    return null;
  }
  const deps = (pkg["dependencies"] ?? {}) as Record<string, string>;
  const current = deps["zod"];
  if (current && zodRangeExposesAiSdkSubpaths(current)) return pkgJson;
  pkg["dependencies"] = Object.fromEntries(
    Object.entries({ ...deps, zod: AI_SDK_ZOD_VERSION }).sort(([a], [b]) => a.localeCompare(b)),
  );
  return JSON.stringify(pkg, null, 2) + "\n";
}

export function zodRangeExposesAiSdkSubpaths(range: string): boolean {
  const match = range.match(/(\d+)\.(\d+)\.(\d+)/);
  if (!match) return false;
  const major = Number(match[1]);
  const minor = Number(match[2]);
  const patch = Number(match[3]);
  if (!Number.isFinite(major) || !Number.isFinite(minor) || !Number.isFinite(patch)) return false;
  if (major > 3) return true;
  if (major < 3) return false;
  if (minor > 25) return true;
  if (minor < 25) return false;
  return patch >= 76;
}

