/**
 * The Next.js (App Router) wiring codemod — the shadcn/next-auth-style half of
 * `vendo init`. Writes the pieces a host app needs to run Vendo:
 *
 *   1. `app/api/vendo/[...path]/route.ts` — `createVendoHandler()`
 *   2. `app/vendo-root.tsx` — client wrapper importing `.vendo/` JSON
 *   3. root layout edit — wraps `{children}` with the wrapper (idempotent)
 *   4. `instrumentation.ts` (or `src/instrumentation.ts`) — boots the
 *      automation scheduler via `startVendoScheduler()` in the Node.js
 *      runtime
 *   5. `.env.example` — capability-additive keys + storage/scheduler docs
 *   6. `public/vendo/` — sandbox runtime assets (React shim + catalog bundle)
 *   7. `package.json` — adds `vendoai` to dependencies
 *
 * SAFETY CONTRACT: never break existing code. Every step that cannot proceed
 * with certainty (unrecognizable layout, ambiguous `{children}`, missing app
 * dir) is SKIPPED and reported with exact manual instructions instead of
 * guessing. All edits are plain files reviewable in one `git diff`.
 */
