export type NextConfigMergeResult =
  | { kind: "updated"; source: string }
  | { kind: "unchanged"; source: string }
  | { kind: "skipped"; reason: string };

function nextConfigScriptKind(fileName: string): ts.ScriptKind {
  if (fileName.endsWith(".ts")) return ts.ScriptKind.TS;
  if (fileName.endsWith(".tsx")) return ts.ScriptKind.TSX;
  if (fileName.endsWith(".jsx")) return ts.ScriptKind.JSX;
  return ts.ScriptKind.JS;
}

function unwrapConfigExpression(expr: ts.Expression): ts.Expression {
  let cur = expr;
  while (
    ts.isParenthesizedExpression(cur) ||
    ts.isAsExpression(cur) ||
    ts.isSatisfiesExpression(cur) ||
    ts.isTypeAssertionExpression(cur) ||
    ts.isNonNullExpression(cur)
  ) {
    if (ts.isParenthesizedExpression(cur) || ts.isNonNullExpression(cur)) cur = cur.expression;
    else cur = cur.expression;
  }
  return cur;
}

function isModuleExports(expr: ts.Expression): boolean {
  return (
    ts.isPropertyAccessExpression(expr) &&
    expr.name.text === "exports" &&
    ts.isIdentifier(expr.expression) &&
    expr.expression.text === "module"
  );
}

function propertyNameText(name: ts.PropertyName): string | null {
  if (ts.isIdentifier(name) || ts.isStringLiteral(name) || ts.isNumericLiteral(name)) return name.text;
  return null;
}

function lineLeadingIndent(source: string, pos: number): string {
  const lineStart = source.lastIndexOf("\n", Math.max(0, pos - 1)) + 1;
  const m = source.slice(lineStart, pos).match(/^[ \t]*/);
  return m?.[0] ?? "";
}

function visualIndentAt(sf: ts.SourceFile, pos: number): string {
  return " ".repeat(sf.getLineAndCharacterOfPosition(pos).character);
}

function findConfigObject(
  source: string,
  fileName: string,
): { object: ts.ObjectLiteralExpression; sf: ts.SourceFile } | { reason: string } {
  const sf = ts.createSourceFile(
    fileName,
    source,
    ts.ScriptTarget.Latest,
    true,
    nextConfigScriptKind(fileName),
  );
  const objectVars = new Map<string, ts.ObjectLiteralExpression>();
  for (const stmt of sf.statements) {
    if (!ts.isVariableStatement(stmt)) continue;
    for (const decl of stmt.declarationList.declarations) {
      if (!ts.isIdentifier(decl.name) || !decl.initializer) continue;
      const init = unwrapConfigExpression(decl.initializer);
      if (ts.isObjectLiteralExpression(init)) objectVars.set(decl.name.text, init);
    }
  }

  const candidates: ts.ObjectLiteralExpression[] = [];
  let sawConfigExport = false;
  const resolve = (expr: ts.Expression): ts.ObjectLiteralExpression | null => {
    const unwrapped = unwrapConfigExpression(expr);
    if (ts.isObjectLiteralExpression(unwrapped)) return unwrapped;
    if (ts.isIdentifier(unwrapped)) return objectVars.get(unwrapped.text) ?? null;
    return null;
  };

  for (const stmt of sf.statements) {
    if (ts.isExportAssignment(stmt)) {
      sawConfigExport = true;
      const object = resolve(stmt.expression);
      if (object) candidates.push(object);
      continue;
    }
    if (!ts.isExpressionStatement(stmt) || !ts.isBinaryExpression(stmt.expression)) continue;
    const expr = stmt.expression;
    if (expr.operatorToken.kind !== ts.SyntaxKind.EqualsToken || !isModuleExports(expr.left)) continue;
    sawConfigExport = true;
    const object = resolve(expr.right);
    if (object) candidates.push(object);
  }

  if (candidates.length === 1) return { object: candidates[0]!, sf };
  if (candidates.length > 1) return { reason: "multiple plain object config exports found" };
  return {
    reason: sawConfigExport
      ? "config export is not a plain object literal"
      : "no export default or module.exports config object found",
  };
}

type StringArrayProperty =
  | { kind: "present"; property: ts.PropertyAssignment; array: ts.ArrayLiteralExpression; values: string[] }
  | { kind: "absent" }
  | { kind: "skipped"; reason: string };

function inspectStringArrayProperty(
  object: ts.ObjectLiteralExpression,
  propName: string,
): StringArrayProperty {
  let found: ts.ObjectLiteralElementLike | null = null;
  for (const prop of object.properties) {
    if (ts.isSpreadAssignment(prop)) {
      return { kind: "skipped", reason: "config object uses object spread (...)" };
    }
    const name = prop.name ? propertyNameText(prop.name) : null;
    if (prop.name && name === null) {
      return { kind: "skipped", reason: "config object has computed property names" };
    }
    if (name !== propName) continue;
    if (found) return { kind: "skipped", reason: `${propName} is declared more than once` };
    found = prop;
  }
  if (!found) return { kind: "absent" };
  if (!ts.isPropertyAssignment(found)) {
    return { kind: "skipped", reason: `${propName} is not a property assignment` };
  }
  const init = unwrapConfigExpression(found.initializer);
  if (!ts.isArrayLiteralExpression(init)) {
    return { kind: "skipped", reason: `${propName} is not a string array literal` };
  }
  const values: string[] = [];
  for (const element of init.elements) {
    const item = unwrapConfigExpression(element as ts.Expression);
    if (ts.isStringLiteral(item) || ts.isNoSubstitutionTemplateLiteral(item)) {
      values.push(item.text);
      continue;
    }
    return { kind: "skipped", reason: `${propName} contains non-string entries` };
  }
  return { kind: "present", property: found, array: init, values };
}

function mergeValues(existing: readonly string[], required: readonly string[]): string[] {
  const present = new Set(existing);
  return [...existing, ...required.filter((value) => !present.has(value))];
}

function hasEveryValue(existing: readonly string[], required: readonly string[]): boolean {
  const present = new Set(existing);
  return required.every((value) => present.has(value));
}

function renderStringArray(values: readonly string[], propertyIndent: string): string {
  return `[\n${values.map((value) => `${propertyIndent}  ${JSON.stringify(value)},`).join("\n")}\n${propertyIndent}]`;
}

function renderConfigProperty(name: string, values: readonly string[], propertyIndent: string): string {
  return `${propertyIndent}${name}: ${renderStringArray(values, propertyIndent)},`;
}

export function mergeNextConfig(source: string, fileName = "next.config.ts"): NextConfigMergeResult {
  const found = findConfigObject(source, fileName);
  if ("reason" in found) return { kind: "skipped", reason: found.reason };
  const { object, sf } = found;
  const transpile = inspectStringArrayProperty(object, "transpilePackages");
  if (transpile.kind === "skipped") return { kind: "skipped", reason: transpile.reason };
  const external = inspectStringArrayProperty(object, "serverExternalPackages");
  if (external.kind === "skipped") return { kind: "skipped", reason: external.reason };

  const replacements: Array<{ start: number; end: number; text: string }> = [];
  const addedProps: string[] = [];
  const propertyIndent =
    object.properties.length > 0
      ? visualIndentAt(sf, object.properties[0]!.getStart(sf))
      : lineLeadingIndent(source, object.getStart(sf)) + "  ";
  const closeIndent = lineLeadingIndent(source, object.getStart(sf));

  if (transpile.kind === "present") {
    if (!hasEveryValue(transpile.values, VENDO_TRANSPILE_PACKAGES)) {
      replacements.push({
        start: transpile.array.getStart(sf),
        end: transpile.array.getEnd(),
        text: renderStringArray(mergeValues(transpile.values, VENDO_TRANSPILE_PACKAGES), propertyIndent),
      });
    }
  } else {
    addedProps.push(renderConfigProperty("transpilePackages", VENDO_TRANSPILE_PACKAGES, propertyIndent));
  }

  if (external.kind === "present") {
    if (!hasEveryValue(external.values, NEXT_SERVER_EXTERNAL_PACKAGES)) {
      replacements.push({
        start: external.array.getStart(sf),
        end: external.array.getEnd(),
        text: renderStringArray(mergeValues(external.values, NEXT_SERVER_EXTERNAL_PACKAGES), propertyIndent),
      });
    }
  } else {
    addedProps.push(renderConfigProperty("serverExternalPackages", NEXT_SERVER_EXTERNAL_PACKAGES, propertyIndent));
  }

  if (addedProps.length > 0) {
    const closeBrace = object.getEnd() - 1;
    const lastProp = object.properties[object.properties.length - 1];
    let prefix = "\n";
    if (lastProp) {
      const betweenLastPropAndClose = source.slice(lastProp.getEnd(), closeBrace);
      prefix = betweenLastPropAndClose.trimStart().startsWith(",") ? "\n" : ",\n";
    }
    replacements.push({
      start: closeBrace,
      end: closeBrace,
      text: `${prefix}${addedProps.join("\n")}\n${closeIndent}`,
    });
  }

  if (replacements.length === 0) return { kind: "unchanged", source };
  let next = source;
  for (const r of replacements.sort((a, b) => b.start - a.start)) {
    next = next.slice(0, r.start) + r.text + next.slice(r.end);
  }
  return { kind: next === source ? "unchanged" : "updated", source: next };
}

function minimalNextConfigSource(tsConfig: boolean): string {
  const body = [
    "  transpilePackages: [",
    ...VENDO_TRANSPILE_PACKAGES.map((pkg) => `    ${JSON.stringify(pkg)},`),
    "  ],",
    "  serverExternalPackages: [",
    ...NEXT_SERVER_EXTERNAL_PACKAGES.map((pkg) => `    ${JSON.stringify(pkg)},`),
    "  ],",
  ].join("\n");
  if (tsConfig) {
    return `import type { NextConfig } from "next";\n\nconst nextConfig: NextConfig = {\n${body}\n};\n\nexport default nextConfig;\n`;
  }
  return `/** @type {import("next").NextConfig} */\nconst nextConfig = {\n${body}\n};\n\nexport default nextConfig;\n`;
}

/** Sandbox assets bundled with the CLI at build time (see scripts/bundle-assets.mjs). */
export function bundledAssetsDir(): string {
  return fileURLToPath(new URL("./assets/", import.meta.url));
}

/**
 * The sandbox runtime assets, as `[bundled-name, installed-name]` pairs:
 * copied from {@link bundledAssetsDir} into a host app's `public/vendo/` by
 * `wireNextApp` (step 7) and read back by `vendo doctor` to judge staleness —
 * a SINGLE source of truth so a filename change can't drift the two apart.
 */
export const SANDBOX_ASSETS: ReadonlyArray<readonly [bundled: string, installed: string]> = [
  ["vendo-react-runtime.js", "react-runtime.js"],
  ["vendo-components-sandbox.js", "components-sandbox.js"],
] as const;

export function sandboxAssetSource(source: string): string {
  return source.startsWith("// @ts-nocheck") ? source : `// @ts-nocheck\n${source}`;
}

const MANUAL_LAYOUT = (layoutFile: string) =>
  `wrap your root layout's {children} manually in ${layoutFile}:\n` +
  `    import { AppVendoRoot } from "./vendo-root";\n` +
  `    ...<AppVendoRoot>{children}</AppVendoRoot>...`;

const MANUAL_INSTRUMENTATION = (exampleFile: string, instrumentationFile: string) =>
  `merge the scheduler boot from ${exampleFile} into your existing ${instrumentationFile}'s register() (see vendo's docs)`;

const MANUAL_NEXT_CONFIG = (configFile: string) =>
  `merge Vendo's Next.js settings into ${configFile}: add missing entries to ` +
  `transpilePackages (${VENDO_TRANSPILE_PACKAGES.join(", ")}) and add ` +
  `serverExternalPackages: ["${NEXT_SERVER_EXTERNAL_PACKAGES.join('", "')}"]`;

export async function wireNextApp(
  targetDir: string,
  info: FrameworkInfo,
  opts: { force: boolean },
): Promise<WiringSummary | null> {
  if (info.framework !== "next") return null;
  const found = await findAppDir(targetDir);
  const summary: WiringSummary = { written: [], edited: [], skipped: [], manual: [] };
  if (!found) {
    summary.skipped.push({
      step: "next-wiring",
      reason:
        "no App Router root layout at app/layout.* or src/app/layout.*. " +
        "Pages Router and route-group-only roots (app/(group)/layout.*) aren't " +
        "auto-wired yet — add app/api/vendo/[...path]/route.ts and wrap your " +
        "root layout with <AppVendoRoot> by hand (see vendo's docs).",
    });
    return summary;
  }
  const { appDir, layoutFile } = found;
  const ts = layoutFile.endsWith(".tsx") || (await exists(path.join(targetDir, "tsconfig.json")));
  const rel = (p: string) => path.relative(targetDir, p);

  // 0. Guarantee the .vendo JSON artifacts the wrapper imports exist (the
  // extractor may have skipped them, and a missing JSON import breaks builds).
  const themePath = path.join(targetDir, ".vendo/theme.json");
  if (!(await exists(themePath))) {
    await fs.mkdir(path.dirname(themePath), { recursive: true });
    await fs.writeFile(themePath, JSON.stringify(DEFAULT_THEME_STUB, null, 2) + "\n");
    summary.written.push(rel(themePath));
  }
  const toolsPath = path.join(targetDir, ".vendo/tools.json");
  if (!(await exists(toolsPath))) {
    await fs.writeFile(toolsPath, JSON.stringify({ version: 1, tools: [], events: [] }, null, 2) + "\n");
    summary.written.push(rel(toolsPath));
  }

  // 1. The catch-all route.
  const routeFile = path.join(appDir, "api/vendo/[...path]", ts ? "route.ts" : "route.js");
  if (await exists(routeFile)) {
    summary.skipped.push({ step: "route", reason: `${rel(routeFile)} already exists` });
  } else {
    await fs.mkdir(path.dirname(routeFile), { recursive: true });
    await fs.writeFile(routeFile, ROUTE_SOURCE);
    summary.written.push(rel(routeFile));
  }

  // 2. The client wrapper.
  const rootFile = path.join(appDir, ts ? "vendo-root.tsx" : "vendo-root.jsx");
  const vendoRel = path
    .relative(appDir, path.join(targetDir, ".vendo"))
    .split(path.sep)
    .join("/");
  let productName = "Assistant";
  const pkgRaw = await readIf(path.join(targetDir, "package.json"));
  if (pkgRaw) {
    try {
      productName = productNameFrom((JSON.parse(pkgRaw) as { name?: string }).name);
    } catch {
      /* keep default */
    }
  }
  if (await exists(rootFile)) {
    summary.skipped.push({ step: "vendo-root", reason: `${rel(rootFile)} already exists` });
  } else {
    await fs.writeFile(rootFile, rootSource({ ts, vendoRel, productName }));
    summary.written.push(rel(rootFile));
  }

  // 3. Layout wrap (never rewrites on uncertainty).
  const layoutSource = await readIf(layoutFile);
  if (layoutSource === null) {
    summary.skipped.push({ step: "layout", reason: `${rel(layoutFile)} unreadable` });
    summary.manual.push(MANUAL_LAYOUT(rel(layoutFile)));
  } else {
    const edited = wrapLayoutChildren(layoutSource);
    if (edited === null) {
      summary.skipped.push({
        step: "layout",
        reason: `could not find exactly one {children} in ${rel(layoutFile)} — left untouched`,
      });
      summary.manual.push(MANUAL_LAYOUT(rel(layoutFile)));
    } else if (edited !== layoutSource) {
      await fs.writeFile(layoutFile, edited);
      summary.edited.push(rel(layoutFile));
    } else {
      summary.skipped.push({ step: "layout", reason: "already wired" });
    }
  }

  // 4. instrumentation.ts — boots the automation scheduler in the Node.js
  // runtime. Lives at the project root, or under src/ when the app itself
  // lives under src/app (Next.js instrumentation-file convention).
  const instrumentationDir = path.relative(targetDir, appDir) === "app" ? targetDir : path.join(targetDir, "src");
  const instrumentationFile = path.join(instrumentationDir, ts ? "instrumentation.ts" : "instrumentation.js");
  const instrumentationExisting = await readIf(instrumentationFile);
  if (instrumentationExisting === null) {
    await fs.mkdir(instrumentationDir, { recursive: true });
    await fs.writeFile(instrumentationFile, INSTRUMENTATION_SOURCE);
    summary.written.push(rel(instrumentationFile));
  } else {
    const merged = mergeInstrumentation(instrumentationExisting);
    if (merged === null) {
      const exampleFile = path.join(
        instrumentationDir,
        ts ? "instrumentation.vendo-example.ts" : "instrumentation.vendo-example.js",
      );
      if (await exists(exampleFile)) {
        summary.skipped.push({ step: "instrumentation", reason: `${rel(exampleFile)} already exists` });
      } else {
        await fs.writeFile(exampleFile, INSTRUMENTATION_SOURCE);
        summary.written.push(rel(exampleFile));
      }
      summary.manual.push(MANUAL_INSTRUMENTATION(rel(exampleFile), rel(instrumentationFile)));
    } else if (merged !== instrumentationExisting) {
      await fs.writeFile(instrumentationFile, merged);
      summary.edited.push(rel(instrumentationFile));
    } else {
      summary.skipped.push({ step: "instrumentation", reason: "already wired" });
    }
  }

  // 4b. experimental.instrumentationHook — Next 13/14 load instrumentation.ts
  // ONLY with this flag; 15+ ignore it (default on). Version-gated so we never
  // touch a config that doesn't need it. Undetermined version → treat as
  // needing it (the flag is harmless on 15+). Following the codemod's fail-safe
  // convention, an EXISTING config is never AST-edited — we skip + print a
  // manual step; only an absent config is written for us.
  const nextMajor = pkgRaw ? nextMajorVersion(pkgRaw) : undefined;
  if (nextMajor === undefined || nextMajor < 15) {
    const configNames = ["next.config.js", "next.config.mjs", "next.config.ts", "next.config.cjs"];
    let configFile: string | undefined;
    for (const name of configNames) {
      if (await exists(path.join(targetDir, name))) {
        configFile = path.join(targetDir, name);
        break;
      }
    }
    if (!configFile) {
      const dest = path.join(targetDir, "next.config.mjs");
      await fs.writeFile(dest, NEXT_CONFIG_SOURCE);
      summary.written.push(rel(dest));
    } else {
      const configSrc = (await readIf(configFile)) ?? "";
      if (maskLiterals(configSrc).includes("instrumentationHook")) {
        summary.skipped.push({ step: "next.config", reason: `${rel(configFile)} already sets instrumentationHook` });
      } else {
        summary.skipped.push({
          step: "next.config",
          reason: `${rel(configFile)} exists — left untouched (Next <15 needs experimental.instrumentationHook)`,
        });
        summary.manual.push(
          `add experimental: { instrumentationHook: true } to ${rel(configFile)} (required on Next 13/14 so instrumentation.ts loads)`,
        );
      }
    }
  }

  // 5. .env.example — create, or append each Vendo block once. Independent
  // sentinels (ANTHROPIC_API_KEY / DATABASE_URL) so a project wired by an
  // older codemod version gains the newer capability-additive block on re-run.
  const envFile = path.join(targetDir, ".env.example");
  const envExisting = await readIf(envFile);
  if (envExisting === null) {
    await fs.writeFile(envFile, ENV_EXAMPLE + "\n" + STORAGE_ENV);
    summary.written.push(rel(envFile));
  } else {
    let nextEnv = envExisting;
    if (!nextEnv.includes("Vendo — capability-additive keys")) {
      nextEnv = nextEnv.replace(/\n?$/, "\n\n") + ENV_EXAMPLE;
    }
    if (!nextEnv.includes("DATABASE_URL")) nextEnv = nextEnv.replace(/\n?$/, "\n\n") + STORAGE_ENV;
    if (nextEnv !== envExisting) {
      await fs.writeFile(envFile, nextEnv);
      summary.edited.push(rel(envFile));
    } else {
      summary.skipped.push({ step: ".env.example", reason: "already documents Vendo's provider keys and DATABASE_URL" });
    }
  }

  // 6. next.config.* — Vendo's published packages need transpilation in host
  // apps, and PGlite must stay external so its import.meta.url wasm loading
  // survives Next's server bundling.
  const configFiles = ["next.config.ts", "next.config.mjs", "next.config.js"]
    .map((name) => path.join(targetDir, name));
  const existingConfigFiles: string[] = [];
  for (const configFile of configFiles) {
    if (await exists(configFile)) existingConfigFiles.push(configFile);
  }
  if (existingConfigFiles.length === 0) {
    const configFile = path.join(targetDir, ts ? "next.config.ts" : "next.config.mjs");
    await fs.writeFile(configFile, minimalNextConfigSource(ts));
    summary.written.push(rel(configFile));
  } else if (existingConfigFiles.length > 1) {
    const names = existingConfigFiles.map((file) => rel(file)).join(", ");
    summary.skipped.push({ step: "next.config", reason: `multiple Next config files found (${names})` });
    summary.manual.push(MANUAL_NEXT_CONFIG(names));
  } else {
    const configFile = existingConfigFiles[0]!;
    const configSource = await readIf(configFile);
    if (configSource === null) {
      summary.skipped.push({ step: "next.config", reason: `${rel(configFile)} unreadable` });
      summary.manual.push(MANUAL_NEXT_CONFIG(rel(configFile)));
    } else {
      const merged = mergeNextConfig(configSource, rel(configFile));
      if (merged.kind === "skipped") {
        summary.skipped.push({ step: "next.config", reason: `${rel(configFile)}: ${merged.reason}` });
        summary.manual.push(MANUAL_NEXT_CONFIG(rel(configFile)));
      } else if (merged.kind === "updated") {
        await fs.writeFile(configFile, merged.source);
        summary.edited.push(rel(configFile));
      } else {
        summary.skipped.push({
          step: "next.config",
          reason: "already includes Vendo package transpilation and PGlite externalization",
        });
      }
    }
  }

  // 7. Sandbox assets into public/vendo/.
  const assetsDir = bundledAssetsDir();
  const publicDir = path.join(targetDir, "public/vendo");
  for (const [from, to] of SANDBOX_ASSETS) {
    const src = path.join(assetsDir, from);
    const dest = path.join(publicDir, to);
    if (!(await exists(src))) {
      summary.skipped.push({ step: "sandbox-assets", reason: `bundled asset ${from} missing from CLI build` });
      summary.manual.push(
        "copy the sandbox assets by hand: react-runtime.js + components-sandbox.js into public/vendo/ (see vendo's docs)",
      );
      break;
    }
    if ((await exists(dest)) && !opts.force) {
      summary.skipped.push({ step: "sandbox-assets", reason: `${rel(dest)} already exists` });
      continue;
    }
    await fs.mkdir(publicDir, { recursive: true });
    await fs.writeFile(dest, sandboxAssetSource(await fs.readFile(src, "utf8")));
    summary.written.push(rel(dest));
  }

  // 8. package.json: vendo + PGlite + AI SDK-compatible Zod dependencies +
  // prebuild sync wiring. PGlite is also server-externalized in next.config,
  // but pnpm's strict node_modules still requires it to be resolvable from the
  // app root.
  if (pkgRaw) {
    const withDep = addDependency(pkgRaw, "vendoai", "latest");
    if (withDep === null) {
      summary.skipped.push({ step: "package.json", reason: "unparsable — add vendoai and @electric-sql/pglite yourself" });
      summary.manual.push('add "vendoai" to package.json dependencies and install');
      summary.manual.push('add "@electric-sql/pglite" to package.json dependencies and install');
      summary.manual.push('add "zod": "^3.25.76" to package.json dependencies and install');
      summary.manual.push('add "vendo sync" to your package.json "prebuild" script');
      summary.manual.push('add "@vendoai/cli" to package.json devDependencies (the prebuild `vendo sync` runner)');
    } else {
      // @vendoai/cli is a devDependency: `prebuild: vendo sync` needs it to
      // resolve at build time. PGlite is a runtime dependency for the store.
      const withCli = addDevDependency(withDep, "@vendoai/cli", "latest") ?? withDep;
      const withPglite = addDependency(withCli, PGLITE_DEPENDENCY, PGLITE_VERSION) ?? withCli;
      const withZod = ensureAiSdkZodDependency(withPglite) ?? withPglite;
      const withSync = addPrebuildSync(withZod) ?? withZod;
      if (withSync !== pkgRaw) {
        await fs.writeFile(path.join(targetDir, "package.json"), withSync);
        summary.edited.push("package.json");
        if (withZod !== pkgRaw) {
          summary.manual.push("run your package manager's install (npm/pnpm/yarn) to pull vendoai, @electric-sql/pglite, and zod");
        }
      } else {
        summary.skipped.push({ step: "package.json", reason: "vendoai + @electric-sql/pglite + zod + prebuild sync already present" });
      }
    }
  }

  return summary;
}

/**
 * Render a wiring summary for the console. `omitSkips` (catch-up mode) drops the
 * idempotent "skip … already exists / already wired" lines, keeping only actual
 * changes (written/edited) and manual TODOs — the caller only reaches this with
 * omitSkips when at least one of those is present, so the line list is never
 * empty. Default (first-run init) keeps skips so its output is unchanged.
 */
export function renderWiring(w: WiringSummary | null, opts?: { omitSkips?: boolean }): string {
  if (!w) return "";
  const lines: string[] = [];
  for (const f of w.written) lines.push(`  wrote  ${f}`);
  for (const f of w.edited) lines.push(`  edited ${f}`);
  if (!opts?.omitSkips) {
    for (const s of w.skipped) lines.push(`  skip   ${s.step}: ${s.reason}`);
  }
  if (w.manual.length > 0) {
    lines.push("  TODO (manual):");
    for (const m of w.manual) lines.push(`   - ${m}`);
  }
  if (lines.length === 0) return "";
  return ["next wiring:", ...lines].join("\n");
}
