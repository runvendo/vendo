import { mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { build } from "vite";

const packageDir = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const entry = resolve(packageDir, "src/tree/jail/runtime-entry.tsx");
const output = resolve(packageDir, "src/tree/jail/runtime-bundle.gen.ts");

const result = await build({
  configFile: false,
  root: packageDir,
  define: { "process.env.NODE_ENV": JSON.stringify("production") },
  build: {
    write: false,
    minify: "esbuild",
    target: "es2020",
    lib: {
      entry,
      name: "VendoJailRuntime",
      formats: ["iife"],
      fileName: () => "vendo-jail-runtime.js",
    },
    rollupOptions: {
      output: { inlineDynamicImports: true },
    },
  },
});

const outputs = Array.isArray(result) ? result.flatMap((entryResult) => entryResult.output) : result.output;
const chunk = outputs.find((item) => item.type === "chunk");
if (!chunk || chunk.type !== "chunk") throw new Error("Vite did not emit the jail runtime chunk");

await mkdir(dirname(output), { recursive: true });
await writeFile(
  output,
  `/** Generated by scripts/build-jail-runtime.mjs. Do not edit. */\nexport const JAIL_RUNTIME_SOURCE = ${JSON.stringify(chunk.code)};\n`,
  "utf8",
);
console.log(`[ui] generated jail runtime (${chunk.code.length} bytes)`);
