/**
 * Regenerates src/chrome/onest-font.gen.ts — the Onest variable font (latin +
 * latin-ext subsets) inlined as data-URI @font-face rules, prepended to the
 * chrome stylesheet so the unthemed default look renders the brand font with
 * zero host setup.
 *
 * Onest is licensed under the SIL Open Font License 1.1
 * (Copyright 2021 The Onest Project Authors, https://github.com/googlefonts/onest);
 * the license text ships in packages/ui/ONEST-OFL.txt.
 *
 * Run: pnpm --filter @vendoai/ui build:onest-font
 */
import { writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";

const packageDir = resolve(fileURLToPath(new URL(".", import.meta.url)), "..");
const output = resolve(packageDir, "src/chrome/onest-font.gen.ts");

// Pinned Onest v9 variable-weight (100-900) subsets from Google Fonts. Pinned
// so regeneration is deterministic; bump deliberately when the family updates.
// Data-URI faces require the host CSP to allow font-src data: — when a strict
// host blocks them, the chrome falls back to the system stack in
// defaultVendoTheme (graceful; no console noise beyond the CSP report).
const SUBSETS = [
  {
    name: "latin",
    url: "https://fonts.gstatic.com/s/onest/v9/gNMKW3F-SZuj7xmf-HYoEoey.woff2",
    range: "U+0000-00FF, U+0131, U+0152-0153, U+02BB-02BC, U+02C6, U+02DA, U+02DC, U+0304, U+0308, U+0329, U+2000-206F, U+20AC, U+2122, U+2191, U+2193, U+2212, U+2215, U+FEFF, U+FFFD",
  },
  {
    name: "latin-ext",
    url: "https://fonts.gstatic.com/s/onest/v9/gNMKW3F-SZuj7xmR-HYoEoeyxMI.woff2",
    range: "U+0100-02BA, U+02BD-02C5, U+02C7-02CC, U+02CE-02D7, U+02DD-02FF, U+0304, U+0308, U+0329, U+1D00-1DBF, U+1E00-1E9F, U+1EF2-1EFF, U+2020, U+20A0-20AB, U+20AD-20C0, U+2113, U+2C60-2C7F, U+A720-A7FF",
  },
];

const faces = [];
for (const subset of SUBSETS) {
  const response = await fetch(subset.url);
  if (!response.ok) throw new Error(`fetch ${subset.url}: ${response.status}`);
  const bytes = Buffer.from(await response.arrayBuffer());
  faces.push(`/* ${subset.name} */
@font-face {
  font-family: 'Onest';
  font-style: normal;
  font-weight: 100 900;
  font-display: swap;
  src: url(data:font/woff2;base64,${bytes.toString("base64")}) format('woff2');
  unicode-range: ${subset.range};
}`);
  console.log(`${subset.name}: ${bytes.length} bytes`);
}

const banner = `/**
 * Generated by packages/ui/scripts/build-onest-font.mjs. Do not edit.
 *
 * Onest (SIL OFL 1.1 — see packages/ui/ONEST-OFL.txt), latin + latin-ext
 * variable-weight subsets, inlined so the default look needs no host setup.
 *
 * Regenerate: pnpm --filter @vendoai/ui build:onest-font
 */
export const ONEST_FONT_CSS: string = ${JSON.stringify(faces.join("\n") + "\n")};
`;
await writeFile(output, banner);
console.log(`wrote ${output}`);
