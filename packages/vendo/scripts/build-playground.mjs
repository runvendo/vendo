import { mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { build } from "vite";

// Same pattern as packages/ui/scripts/build-jail-runtime.mjs: bundle the
// playground page (real chrome + scripted transport + fake client) into one
// self-contained IIFE and ship it as a generated source string, so the CLI
// serves it with zero runtime asset plumbing. The embed entry (docs inline
// mounting, window.VendoDocsEmbed) builds the same way into its own string.
const packageDir = resolve(dirname(fileURLToPath(import.meta.url)), "..");

const BUNDLES = [
  {
    entry: resolve(packageDir, "src/cli/playground/app/main.tsx"),
    output: resolve(packageDir, "src/cli/playground/bundle.gen.ts"),
    name: "VendoPlayground",
    constant: "PLAYGROUND_BUNDLE_SOURCE",
    label: "playground bundle",
  },
  {
    entry: resolve(packageDir, "src/cli/playground/app/embed-entry.tsx"),
    output: resolve(packageDir, "src/cli/playground/embed-bundle.gen.ts"),
    name: "VendoDocsEmbed",
    constant: "EMBED_BUNDLE_SOURCE",
    label: "embed bundle",
  },
];

for (const bundle of BUNDLES) {
  const result = await build({
    configFile: false,
    root: packageDir,
    logLevel: "warn",
    define: { "process.env.NODE_ENV": JSON.stringify("production") },
    build: {
      write: false,
      minify: "esbuild",
      target: "es2020",
      lib: {
        entry: bundle.entry,
        name: bundle.name,
        formats: ["iife"],
        fileName: () => "bundle.js",
      },
      rollupOptions: {
        output: { inlineDynamicImports: true },
        // "use client" directives in bundled deps are meaningless in an IIFE —
        // drop the warning wall so real warnings stay visible.
        onwarn: (warning, warn) => {
          if (warning.code !== "MODULE_LEVEL_DIRECTIVE") warn(warning);
        },
      },
    },
  });

  const outputs = Array.isArray(result) ? result.flatMap((entryResult) => entryResult.output) : result.output;
  const chunk = outputs.find((item) => item.type === "chunk");
  if (!chunk || chunk.type !== "chunk") throw new Error(`Vite did not emit the ${bundle.label} chunk`);

  await mkdir(dirname(bundle.output), { recursive: true });
  await writeFile(
    bundle.output,
    `/** Generated by scripts/build-playground.mjs. Do not edit. */\nexport const ${bundle.constant} = ${JSON.stringify(chunk.code)};\n`,
    "utf8",
  );
  console.log(`[vendo] generated ${bundle.label} (${chunk.code.length} bytes)`);
}
