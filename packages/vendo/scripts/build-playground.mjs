import { mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { build } from "vite";

// Same pattern as packages/ui/scripts/build-jail-runtime.mjs: bundle the
// playground page (real chrome + scripted transport + fake client) into one
// self-contained IIFE and ship it as a generated source string, so the CLI
// serves it with zero runtime asset plumbing.
const packageDir = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const entry = resolve(packageDir, "src/cli/playground/app/main.tsx");
const output = resolve(packageDir, "src/cli/playground/bundle.gen.ts");

const result = await build({
  configFile: false,
  root: packageDir,
  logLevel: "warn",
  define: { "process.env.NODE_ENV": JSON.stringify("production") },
  build: {
    write: false,
    minify: "esbuild",
    target: "es2020",
    lib: {
      entry,
      name: "VendoPlayground",
      formats: ["iife"],
      fileName: () => "vendo-playground.js",
    },
    rollupOptions: {
      output: { inlineDynamicImports: true },
      // "use client" directives in bundled deps are meaningless in an IIFE —
      // drop the warning wall so real warnings stay visible.
      onwarn: (warning, warn) => {
        if (warning.code !== "MODULE_LEVEL_DIRECTIVE") warn(warning);
      },
    },
  },
});

const outputs = Array.isArray(result) ? result.flatMap((entryResult) => entryResult.output) : result.output;
const chunk = outputs.find((item) => item.type === "chunk");
if (!chunk || chunk.type !== "chunk") throw new Error("Vite did not emit the playground chunk");

await mkdir(dirname(output), { recursive: true });
await writeFile(
  output,
  `/** Generated by scripts/build-playground.mjs. Do not edit. */\nexport const PLAYGROUND_BUNDLE_SOURCE = ${JSON.stringify(chunk.code)};\n`,
  "utf8",
);
console.log(`[vendo] generated playground bundle (${chunk.code.length} bytes)`);
